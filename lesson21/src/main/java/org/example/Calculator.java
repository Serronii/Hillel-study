package org.example;

public class Calculator {

    public int summ(int a, int b){
        return a + b;
    }

    public int div(int a, int b){
        return a / b;
    }
    public int subtraction (int a, int b){
        return a - b;
    }
    public int multiplication (int a, int b){
        return a * b;
    }

}
