package org.example;

public class Operation {

    public static void main(String[] args) {
        int a = 10;
        int b = 12;

        int sum = a + b;
        System.out.println(sum);

        int diff = a - b;
        System.out.println(diff);

        int mult = a * b;
        System.out.println(mult);

        float dev = (float) a / b;
        System.out.println(dev);


    }
}
